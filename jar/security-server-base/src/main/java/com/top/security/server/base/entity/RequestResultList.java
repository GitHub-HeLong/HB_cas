package com.top.security.server.base.entity;

import java.util.List;

public class RequestResultList<T> extends Result {
	
	private List<T> result;

	public List<T> getResult() {
		return result;
	}

	public void setResult(List<T> result) {
		this.result = result;
	}
	
	@Override
	public String toString() {
		return "RequestResultList [resultCode=" + resultCode + ", resultStatus=" + resultStatus + ", resultMsg=" + resultMsg
				+ ", result=" + result == null ? "null" : result.toString() + "]";
	}
}

package org.jasig.cas;

public class LoginLog {
	private String id;
	
	private String username;
	
	private String clientIp;
	
	private String serverIp;
	
	private String sysType;
	
	private String loginTime;
	
	private String status = "";
	
	private String logoutTime = "";
	
	private String logoutWay = "";
	
	private String logoutCode = "";
	
	public LoginLog(String id, String username, String clientIp,
			String serverIp, String sysType, String loginTime, String status) {
		super();
		this.id = id;
		this.username = username;
		this.clientIp = clientIp;
		this.serverIp = serverIp;
		this.sysType = sysType;
		this.loginTime = loginTime;
		this.status = status;
	}
	
	

	public LoginLog(String id, String username, String status,
			String logoutTime, String logoutWay, String logoutCode) {
		super();
		this.id = id;
		this.username = username;
		this.status = status;
		this.logoutTime = logoutTime;
		this.logoutWay = logoutWay;
		this.logoutCode = logoutCode;
	}



	public LoginLog() {
		super();
		// TODO Auto-generated constructor stub
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getClientIp() {
		return clientIp;
	}

	public void setClientIp(String clientIp) {
		this.clientIp = clientIp;
	}

	public String getServerIp() {
		return serverIp;
	}

	public void setServerIp(String serverIp) {
		this.serverIp = serverIp;
	}

	public String getSysType() {
		return sysType;
	}

	public void setSysType(String sysType) {
		this.sysType = sysType;
	}

	public String getLoginTime() {
		return loginTime;
	}

	public void setLoginTime(String loginTime) {
		this.loginTime = loginTime;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getLogoutTime() {
		return logoutTime;
	}

	public void setLogoutTime(String logoutTime) {
		this.logoutTime = logoutTime;
	}

	public String getLogoutWay() {
		return logoutWay;
	}

	public void setLogoutWay(String logoutWay) {
		this.logoutWay = logoutWay;
	}
	
	public String getLogoutCode() {
		return logoutCode;
	}

	public void setLogoutCode(String logoutCode) {
		this.logoutCode = logoutCode;
	}

	@Override
	public String toString() {
		return "LoginLog [id=" + id + ", username=" + username + ", clientIp="
				+ clientIp + ", serverIp=" + serverIp + ", sysType=" + sysType
				+ ", loginTime=" + loginTime + ", status=" + status
				+ ", logoutTime=" + logoutTime + ", logoutWay=" + logoutWay
				+ ", logoutCode=" + logoutCode + "]";
	}

}
